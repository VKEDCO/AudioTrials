    public static double keyNumberToKeyFreq(int keyNum) {
        return keyNameToKeyFreq(keyNumberToLetterKeyName(keyNum));
    }
    
    public static boolean isFreqCloseToFreqOfKeyName(String letterKeyName, double f, double error) {
        double lkf = keyNumberToKeyFreq(keyNameToKeyNumber(letterKeyName));
        return Math.abs(lkf - f) <= error;
    }
    
    public static int freqToClosestKeyNumber(double f, double error) {
        for(String letterName: LETTER_KEY_NAMES) {
            if ( isFreqCloseToFreqOfKeyName(letterName, f, error) )
                return keyNameToKeyNumber(letterName);
        }
        return -1;
    }
    
